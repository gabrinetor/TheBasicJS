// Operadores de Igualdade

// Igualdade Estrita (além dos valores, tá comparando o tipo)
console.log( 1 === 1 )  /* true */ //usar sempre esse!!!
console.log( '1' === 1 )  /* false */

// Igualdade solta (ignora tipo e compara apenas valores, converte pra que tipos fiquem iguais)
console.log( 1 == 1 )   /* true */
console.log( '1' == 1 ) /* true */ //não é recomendado