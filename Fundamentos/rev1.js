// Operadores Aritméticos (mat)
//+, - , *, /, **, %
let salario = 100

console.log(salario + salario)  /* 200 */
console.log(salario - salario)  /* 0 */
console.log(salario * salario)  /* 10000 */
console.log(salario / salario)  /* 1 */
console.log(salario ** salario)  /* 1e+200 */
console.log(salario % salario)  /* 0 */
console.log(5 ** 5) /* 3125 */ //exponencial

// ++ -- incremento e decremento
let idade = 19  
let nota = 5

console.log(idade--)  /* 19 */ //depois da operação
console.log(idade)  /* 18 */

console.log(++nota)  /* 6 */
console.log(nota)  /* 6 */